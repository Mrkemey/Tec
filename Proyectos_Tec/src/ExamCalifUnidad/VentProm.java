    public boolean ncalf()throws ExamException{
        if (tblProd.getRowCount()<=6) {
            return true;
        }else{
            throw new ExamException("No se puede agregar mas");
        }
    }
    public void validunid()throws ExamException{
        int temp[]=new int[6];
        for (int i = 0; i < tblProd.getRowCount(); i++) {
            int val=Integer.parseInt((String)modelo.getValueAt(i, 0));
            temp[i]=val;
            
                if (temp[i]==cmbUni.getSelectedIndex()) {
                    throw new ExamException("No se puede agregar otra vez");
                }
            
            
        }
    }
    public void selUni()throws ExamException{
        if (cmbUni.getSelectedIndex()==0) {
            throw new ExamException("Escoja una unidad");
        }
        
    }
    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
        try {
            // TODO add your handling code here:
            if (ncalf()) {
                selUni();
                String cad=(String) cmbUni.getSelectedItem();
                String A[]=cad.split(" ");
                validarcalif();
                validunid();
                O[0]=A[0];
                modelo.addRow(O);
                float prom=st/6;
                txtProm.setText(prom+" ");
            }
            
        } catch (ExamException ex) {
            showMessageDialog(rootPane, ex);
        }
            
    }//GEN-LAST:event_btnAgregarActionPerformed


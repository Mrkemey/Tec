    public void validarcalif() throws ExamException{
        int a=Integer.parseInt(txtCalif.getText());
        if (rbtnOrd.isSelected()) {
            if (a>=70&&a<=100||a==0) {
                O[1]=txtCalif.getText();
                O[2]=" ";
                O[3]=" ";
                st=st+Integer.parseInt(txtCalif.getText());
            }else{
                throw new ExamException("Escriba un numero valido");
            }
        }else{
            if(rbtnRev.isSelected()){
                if (a>=70&&a<=90||a==0) {
                    O[1]=" ";
                    O[2]=txtCalif.getText();
                    O[3]=" ";
                    st=st+Integer.parseInt(txtCalif.getText());
                }else{
                    throw new ExamException("Escriba un numero valido");
                }
            }else{
                if (a>=70&&a<=80||a==0) {
                    O[1]=" ";
                    O[3]=txtCalif.getText();
                    O[2]=" ";
                    st=st+Integer.parseInt(txtCalif.getText());
                }else{
                    throw new ExamException("Escriba un numero valido");
                }
            }
        }
        
   }
